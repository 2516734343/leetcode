var isValid = function(s) {
    let stack = [], i = 0;

    while (i < s.length) {
        // å·¦æ‹¬å·å…¥æ ˆ
        if (s[i] === '(' || s[i] === '[' || s[i] === '{') {
            stack.push(s[i]);
        } else {
            if (stack.length > 0 && leftOf(s[i]) === stack[stack.length - 1]) {
                stack.pop();
            } else {
                return false;
            }
        }
        i++;
    }
    return stack.length === 0;

    function leftOf(c) {
        if (c === ')') return '(';
        if (c === '}') return '{';
        if (c === ']') return '[';
    }

};

